function applyTranslations(language) {
  const elements = document.querySelectorAll("[data-i18n]");

  elements.forEach((element) => {
    const key = element.getAttribute("data-i18n");
    const translatedText = translations[language]?.[key];

    if (translatedText) {
      element.textContent = translatedText;
    }
  });

  localStorage.setItem("preferredLanguage", language);
  document.documentElement.setAttribute("lang", language);
}

document.addEventListener("DOMContentLoaded", () => {
  const languageSwitcher = document.getElementById("languageSwitcher");
  const mobileLanguageSwitcher = document.getElementById(
    "mobileLanguageSwitcher",
  );
  const savedLanguage = localStorage.getItem("preferredLanguage") || "en";

  applyTranslations(savedLanguage);

  if (languageSwitcher) {
    languageSwitcher.value = savedLanguage;
    languageSwitcher.addEventListener("change", (event) => {
      const selected = event.target.value;
      applyTranslations(selected);
      if (mobileLanguageSwitcher) mobileLanguageSwitcher.value = selected;
    });
  }

  if (mobileLanguageSwitcher) {
    mobileLanguageSwitcher.value = savedLanguage;
    mobileLanguageSwitcher.addEventListener("change", (event) => {
      const selected = event.target.value;
      applyTranslations(selected);
      if (languageSwitcher) languageSwitcher.value = selected;
    });
  }
});
